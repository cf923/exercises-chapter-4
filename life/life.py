class Game:
    """Game of life algorithm."""

    def __init__(self, size):
        """Initialise the board."""
        self.board = np.zeros((size, size))

    def play(self):
        """Repeatedly update board."""
        print("Playing life. Press ctrl + c to stop.")
        pyplot.ion()
        while True:
            self.move()
            self.show()
            pyplot.pause(0.0000005)

    def move(self):
        """Update the board."""
        stencil = np.array([[1, 1, 1], [1, 0, 1], [1, 1, 1]])
        neighbourcount = convolve2d(self.board, stencil, mode='same')

        for i in range(self.board.shape[0]):
            for j in range(self.board.shape[1]):
                self.board[i, j] = (neighbourcount[i, j] == 3 or
                                    (neighbourcount[i, j] == 2 and
                                     self.board[i, j]))

    def __setitem__(self, key, value):
        """Change a board element."""
        self.board[key] = value

    def show(self):
        """Show the board."""
        pyplot.clf()
        pyplot.matshow(self.board, fignum=0, cmap='binary')
        pyplot.show()
    def insert(self, patt, square):
        i, j = square[0], square[1]
        self.board[i-1:i+2, j-1:j+2] = patt.grid

def Pattern():
    def __init__(self, patt):
        self.grid = patt
    def flip_vertical(self):
        return Pattern(self.grid[::-1])
    def flip_horizontal(self):
        return Pattern(np.flip(self.grid, axis = -1))
    def flip_diag(self):
        return Pattern(np.transpose(self.grid))
    def rotate(self, n):
        return Pattern(np.rot90(self.grid, k=n, axes = (1, 0)))